def _subtitle_matches_strike(subtitle: str, strike_f: float) -> bool:
    """Check if a Kalshi subtitle corresponds to a given strike temp.

    Subtitles look like: '43° or above', '39° to 40°', '34° or below'.
    For 'X° or above' → strike = X.
    For 'X° to Y°' → strike = Y + 0.5 (bracket midpoint cap).
    """
    if pd.isna(subtitle):
        return False
    s = str(subtitle).strip().replace("°", "")
    # "X or above" → cap_strike = X
    m = re.match(r"(\d+)\s+or\s+above", s)
    if m:
        return float(m.group(1)) == strike_f
    # "X to Y" → cap_strike = Y + 0.5 (common Kalshi convention)
    m = re.match(r"(\d+)\s+to\s+(\d+)", s)
    if m:
        low, high = float(m.group(1)), float(m.group(2))
        # Match if strike is in this bucket range
        return low <= strike_f <= high + 0.5
    # "X or below" → cap_strike = X
    m = re.match(r"(\d+)\s+or\s+below", s)
    if m:
        return float(m.group(1)) == strike_f
    return False


def _parse_strikes_from_snapshots(snapshot_df: pd.DataFrame) -> list[float]:
    """Extract unique strike temperatures from market snapshot subtitles."""
    strikes = set()
    for subtitle in snapshot_df["subtitle"].dropna().unique():
        s = str(subtitle).strip().replace("°", "")
        m = re.match(r"(\d+)\s+or\s+above", s)
        if m:
            strikes.add(float(m.group(1)))
            continue
        m = re.match(r"(\d+)\s+to\s+(\d+)", s)
        if m:
            # Use the high end as the strike
            strikes.add(float(m.group(2)))
            continue
        m = re.match(r"(\d+)\s+or\s+below", s)
        if m:
            strikes.add(float(m.group(1)))
    return sorted(strikes)


def _load_market_snapshots(
    data_dir: Path,
    start_date: date,
    end_date: date,
    series_prefix: str,
) -> pd.DataFrame:
    """Load Kalshi market snapshots for the given date range + filter by series."""
    snap_dir = data_dir / "kalshi" / "market_snapshots"
    if not snap_dir.exists():
        logger.warning("Market snapshot directory not found: %s", snap_dir)
        return pd.DataFrame()

    import pyarrow.parquet as pq
    import pyarrow as pa

    start_str = start_date.isoformat()
    end_str = end_date.isoformat()
    files = sorted(snap_dir.glob("*.parquet"))
    files = [f for f in files if start_str <= f.stem <= end_str]

    if not files:
        logger.warning("No market snapshots for %s → %s.", start_str, end_str)
        return pd.DataFrame()

    tables = [pq.read_table(f) for f in files]
    df = pa.concat_tables(tables, promote_options="default").to_pandas()
    df["snapshot_ts_utc"] = pd.to_datetime(df["snapshot_ts_utc"], utc=True)

    # Filter to relevant series
    df = df[df["event_ticker"].str.startswith(series_prefix)].copy()
    df = df.sort_values("snapshot_ts_utc").reset_index(drop=True)
    logger.info(
        "Loaded %d market snapshots for %s [%s → %s].",
        len(df), series_prefix, start_str, end_str,
    )
    return df


def _find_closest_snapshot(
    snapshots: pd.DataFrame,
    observation_time: pd.Timestamp,
    event_ticker: str,
    lookback_minutes: int = 5,
) -> pd.DataFrame:
    """Find the closest market snapshot BEFORE the observation time.

    Returns the subset of snapshot rows for all strikes at that point.
    We look for the most recent snapshot within `lookback_minutes` of the obs.
    """
    cutoff = observation_time - pd.Timedelta(minutes=lookback_minutes)
    filtered = snapshots[
        (snapshots["event_ticker"] == event_ticker)
        & (snapshots["snapshot_ts_utc"] <= observation_time)
        & (snapshots["snapshot_ts_utc"] >= cutoff)
    ]
    if filtered.empty:
        # Expand search: any snapshot before obs time for this event
        filtered = snapshots[
            (snapshots["event_ticker"] == event_ticker)
            & (snapshots["snapshot_ts_utc"] <= observation_time)
        ]
    if filtered.empty:
        return pd.DataFrame()

    # Get the latest snapshot timestamp and return all rows at that time
    latest_ts = filtered["snapshot_ts_utc"].max()
    return filtered[filtered["snapshot_ts_utc"] == latest_ts]


def _event_ticker_for_date(climate_date_str: str, series_prefix: str) -> str:
    """Generate the expected Kalshi event ticker for a climate date.

    Format: KXHIGHCHI-26FEB21 (last 2 digits of year + uppercase month + day).
    """
    d = date.fromisoformat(climate_date_str)
    return f"{series_prefix}-{d.strftime('%y%b%d').upper()}"


