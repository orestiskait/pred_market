"""Walk-forward backtesting with full P&L simulation.

This script bridges the TWO backtesting systems:
  1. Weather Brain (ML model): services/model/ — trains XGBoost, produces
     probability forecasts per Kalshi strike.
  2. Trading execution: services/backtest/ — simulates fills against
     real Kalshi orderbook snapshots.

Walk-Forward Procedure
======================
                    ┌──────── Train ────────┐┌──── Test (day‑by‑day) ────┐
  Day 1 ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━┃━━━━━━━━━━━━━━━━━━━━━━━━━━━┃
                    TrainingSetBuilder.build  │  For each ASOS-HR obs:     │
                    QuantileSuite.fit         │   1. FeatureEngine → x_t   │
                                              │   2. QuantileSuite → q̂     │
                                              │   3. MonotonicMapper → q̂↑  │
                                              │   4. StrikePricer → P_raw  │
                                              │   5. Calibrator → P_cal    │
                                              │   6. TradingStrategy →     │
                                              │      signals vs market     │
                                              │   7. Settle vs CLI high    │
                                              └────────────────────────────┘

Strategy Abstraction
====================
To test a DIFFERENT model (not XGBoost), create a new subclass of
``TradingStrategy`` and override ``generate_signals()``.  The rest of the
pipeline (data loading, feature engineering, market snapshot lookup,
settlement, P&L reporting) is model-agnostic.

Usage (no CLI arguments — all config is hardcoded below):
    python -m services.model.backtest_pnl
"""

