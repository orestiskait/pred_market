def run_backtest(
    icao: str = ICAO,
    start_date: date = START_DATE,
    end_date: date = END_DATE,
    min_training_days: int = MIN_TRAINING_DAYS,
    strategy: Optional[TradingStrategy] = None,
) -> tuple[list[SimulatedFill], pd.DataFrame]:
    """Run the full walk-forward P&L backtest.

    Returns
    -------
    fills : list[SimulatedFill]
        All simulated trades, settled against CLI.
    summary_df : pd.DataFrame
        Per-day P&L summary.
    """
    # ── Setup ─────────────────────────────────────────────────────────
    config, config_path = load_config()
    data_dir = (config_path.parent / config.get("storage", {}).get("data_dir", "../data")).resolve()

    # Find station metadata
    mc = None
    for cfg in KALSHI_MARKET_REGISTRY.values():
        if cfg.icao == icao:
            mc = cfg
            break
    if mc is None:
        raise ValueError(f"ICAO {icao!r} not found in KALSHI_MARKET_REGISTRY.")

    series_prefix = mc.series_prefix

    if strategy is None:
        strategy = XGBoostEVStrategy(
            xgb_params=XGB_OVERRIDES,
            ev_threshold_cents=EV_THRESHOLD_CENTS,
            max_contracts=MAX_CONTRACTS_PER_SIGNAL,
        )

    # ── Step 1: Build training set (all available data) ───────────────
    logger.info("=" * 65)
    logger.info(" Walk-Forward P&L Backtest")
    logger.info("  Station        : %s (%s)", icao, mc.city)
    logger.info("  Data range     : %s → %s", start_date, end_date)
    logger.info("  Min train days : %d", min_training_days)
    logger.info("  Strategy       : %s", strategy.__class__.__name__)
    logger.info("=" * 65)

    builder = TrainingSetBuilder(
        data_dir=data_dir,
        icao=icao,
        lat=mc.lat,
        lon=mc.lon,
        tz=mc.tz,
    )
    X_full, y_full = builder.build(start_date, end_date, save_log=False)

    if X_full.empty:
        logger.error("Training set is empty — aborting.")
        return [], pd.DataFrame()

    # Print data quality summary
    report = builder.summary_report(X_full, y_full, start_date, end_date)
    logger.info("Data Quality:")
    for k, v in report.items():
        logger.info("  %-35s: %s", k, v)

    all_dates = sorted(X_full["climate_date_lst"].unique())
    n_dates = len(all_dates)
    logger.info("Total climate days with data: %d", n_dates)

    if n_dates < min_training_days + 1:
        logger.error(
            "Not enough days (%d) for train=%d + test≥1. Aborting.",
            n_dates, min_training_days,
        )
        return [], pd.DataFrame()

    # ── Step 2: Split into train / test by date ───────────────────────
    train_dates = all_dates[:min_training_days]
    test_dates = all_dates[min_training_days:]

    train_mask = X_full["climate_date_lst"].isin(train_dates)
    X_train = X_full[train_mask]
    y_train = y_full[train_mask]

    logger.info(
        "Train: %d days (%s → %s), %d rows",
        len(train_dates), train_dates[0], train_dates[-1], len(X_train),
    )
    logger.info(
        "Test:  %d days (%s → %s)",
        len(test_dates), test_dates[0], test_dates[-1],
    )

    # ── Step 3: Train the strategy ────────────────────────────────────
    logger.info("Training model...")
    strategy.fit(X_train, y_train)
    logger.info("Model training complete.")

    # ── Step 4: Load market snapshots for test period ─────────────────
    test_start = date.fromisoformat(test_dates[0])
    test_end = date.fromisoformat(test_dates[-1])
    # Load a buffer around test dates (snapshots start the evening before)
    snap_start = test_start - timedelta(days=1)
    snap_end = test_end + timedelta(days=1)

    snapshots = _load_market_snapshots(data_dir, snap_start, snap_end, series_prefix)

    # ── Step 5: Load CLI labels for settlement ────────────────────────
    loader = ModelDataLoader(data_dir, icao)
    cli_labels = loader.cli_label_map(start_date, end_date)
    logger.info("CLI labels available for %d days.", len(cli_labels))

    # ── Step 6: Walk through test days and generate signals ───────────
    all_fills: list[SimulatedFill] = []
    trade_log: list[dict] = []
    dedup_set: set[tuple[str, str, str]] = set()  # (date, ticker, side) for dedup

    for test_date_str in test_dates:
        test_mask = X_full["climate_date_lst"] == test_date_str
        X_test_day = X_full[test_mask]

        if X_test_day.empty:
            logger.info("[%s] No test rows — skipping.", test_date_str)
            continue

        # Generate the event ticker for this day
        event_ticker = _event_ticker_for_date(test_date_str, series_prefix)

        # Get available strikes from market snapshots for this event
        event_snaps = snapshots[snapshots["event_ticker"] == event_ticker]
        if event_snaps.empty:
            logger.info("[%s] No market snapshots for %s — skipping.", test_date_str, event_ticker)
            continue

        strikes = _parse_strikes_from_snapshots(event_snaps)
        if not strikes:
            logger.info("[%s] No strikes parsed from snapshots — skipping.", test_date_str)
            continue

        logger.info("[%s] %d strikes: %s", test_date_str, len(strikes), strikes)

        # Iterate through observations for the day
        day_signals = 0
        for idx, row in X_test_day.iterrows():
            obs_time = row["observation_time_utc"]
            custom_max = row["custom_intraday_max_f"]

            # Find closest market snapshot BEFORE this observation
            snap_at_obs = _find_closest_snapshot(
                snapshots, obs_time, event_ticker, lookback_minutes=10,
            )
            if snap_at_obs.empty:
                continue

            # Generate signals from strategy
            signals = strategy.generate_signals(
                feature_row=row,
                custom_intraday_max_f=custom_max,
                strikes=strikes,
                market_snapshot=snap_at_obs,
                event_ticker=event_ticker,
                climate_date=test_date_str,
            )

            for signal in signals:
                # Dedup: only trade each (date, market_ticker, side) once
                dedup_key = (test_date_str, signal.market_ticker, signal.side)
                if dedup_key in dedup_set:
                    continue
                dedup_set.add(dedup_key)

                # Simulate execution at the ask price
                if signal.side == "yes":
                    matching = snap_at_obs[
                        snap_at_obs["market_ticker"] == signal.market_ticker
                    ]
                    if matching.empty:
                        continue
                    exec_price = float(matching.iloc[-1]["yes_ask"])
                else:
                    matching = snap_at_obs[
                        snap_at_obs["market_ticker"] == signal.market_ticker
                    ]
                    if matching.empty:
                        continue
                    exec_price = float(matching.iloc[-1]["no_ask"])

                if exec_price <= 0 or exec_price >= 100:
                    continue  # Skip unfillable

                contracts = min(signal.max_contracts, 1)  # Conservative: 1 contract

                fee_cents = _kalshi_taker_fee_cents(contracts, exec_price)
                fill = SimulatedFill(
                    climate_date=test_date_str,
                    observation_time_utc=obs_time,
                    event_ticker=event_ticker,
                    market_ticker=signal.market_ticker,
                    strike_f=signal.strike_f,
                    side=signal.side,
                    contracts=contracts,
                    avg_price_cents=exec_price,
                    total_cost_cents=exec_price * contracts + fee_cents,
                    p_cal=signal.p_cal,
                    ev_cents=signal.ev_cents,
                )
                all_fills.append(fill)
                day_signals += 1

        logger.info("[%s] Filled %d trades.", test_date_str, day_signals)

    # ── Step 7: Settle all fills against CLI labels ───────────────────
    all_fills = _settle_fills(all_fills, cli_labels)

    # ── Step 8: Compute and display P&L ───────────────────────────────
    summary_df = _build_summary(all_fills, cli_labels, test_dates)

    return all_fills, summary_df


# =====================================================================
# ▸ P&L reporting
# =====================================================================

def _build_summary(
    fills: list[SimulatedFill],
    cli_labels: dict[str, int],
    test_dates: list[str],
) -> pd.DataFrame:
    """Build a per-day and aggregate P&L summary."""
    if not fills:
        logger.info("No fills — nothing to summarize.")
        return pd.DataFrame()

    records = []
    for f in fills:
        records.append({
            "climate_date": f.climate_date,
            "observation_time_utc": f.observation_time_utc,
            "event_ticker": f.event_ticker,
            "market_ticker": f.market_ticker,
            "strike_f": f.strike_f,
            "side": f.side,
            "contracts": f.contracts,
            "avg_price_cents": f.avg_price_cents,
            "total_cost_cents": f.total_cost_cents,
            "p_cal": f.p_cal,
            "ev_cents": f.ev_cents,
            "settled": f.settled,
            "settlement_value": f.settlement_value,
            "pnl_cents": f.pnl_cents,
        })

    df = pd.DataFrame(records)

    # ── Print results ─────────────────────────────────────────────────
    print("\n" + "=" * 80)
    print("BACKTEST RESULTS — Walk-Forward P&L")
    print("=" * 80)

    # Per-day summary
    daily = df.groupby("climate_date").agg(
        trades=("pnl_cents", "count"),
        total_cost=("total_cost_cents", "sum"),
        gross_pnl=("pnl_cents", "sum"),
        avg_ev=("ev_cents", "mean"),
        win_rate=("pnl_cents", lambda x: (x > 0).sum() / max(len(x), 1)),
    ).reset_index()

    # Add CLI high for context
    daily["cli_high_f"] = daily["climate_date"].map(cli_labels)

    print("\nPer-Day Summary:")
    print("-" * 80)
    for _, row in daily.iterrows():
        pnl = row["gross_pnl"]
        emoji = "✅" if pnl > 0 else ("⚠️" if pnl == 0 else "❌")
        print(
            f"  {emoji} {row['climate_date']} | CLI High: {row.get('cli_high_f', '?'):>3}°F | "
            f"Trades: {row['trades']:>3} | Cost: ${row['total_cost'] / 100:>7.2f} | "
            f"P&L: ${pnl / 100:>7.2f} | Avg EV: {row['avg_ev']:>5.1f}¢ | "
            f"Win: {row['win_rate']:>5.1%}"
        )

    print("-" * 80)

    # Aggregate summary
    settled = df[df["settled"]]
    total_trades = len(settled)
    total_cost = settled["total_cost_cents"].sum()
    total_pnl = settled["pnl_cents"].sum()
    wins = (settled["pnl_cents"] > 0).sum()
    losses = (settled["pnl_cents"] < 0).sum()
    win_rate = wins / max(total_trades, 1)
    avg_pnl_per_trade = total_pnl / max(total_trades, 1)

    print(f"\n{'Aggregate':^80}")
    print("-" * 80)
    print(f"  Total trades:        {total_trades}")
    print(f"  Wins / Losses:       {wins} / {losses}")
    print(f"  Win rate:            {win_rate:.1%}")
    print(f"  Total cost:          ${total_cost / 100:.2f}")
    print(f"  Gross P&L:           ${total_pnl / 100:.2f}")
    print(f"  P&L per trade:       ${avg_pnl_per_trade / 100:.2f}")
    print(f"  ROI:                 {total_pnl / max(total_cost, 1) * 100:.1f}%")

    # Cumulative P&L series
    print(f"\n  Cumulative P&L by day:")
    cum = 0.0
    for _, row in daily.iterrows():
        cum += row["gross_pnl"]
        bar = "█" * max(0, int(cum / 50))
        print(f"    {row['climate_date']}: ${cum / 100:>8.2f}  {bar}")

    print("=" * 80)

    return df


