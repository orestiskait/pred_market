@dataclass
class TradeSignal:
    """One directional trade signal generated by a strategy."""
    climate_date: str          # LST climate day
    observation_time_utc: datetime
    event_ticker: str          # e.g. KXHIGHCHI-26FEB21
    market_ticker: str         # e.g. KXHIGHCHI-26FEB21-T42
    strike_f: float            # Strike temperature in °F
    side: str                  # "yes" or "no"
    p_cal: float               # Calibrated probability P(High ≥ strike)
    ev_cents: float            # Expected value in cents
    max_contracts: int = 1     # Number of contracts to attempt


@dataclass
class SimulatedFill:
    """One executed paper trade from the backtest."""
    climate_date: str
    observation_time_utc: datetime
    event_ticker: str
    market_ticker: str
    strike_f: float
    side: str
    contracts: int
    avg_price_cents: float     # What we paid per contract
    total_cost_cents: float
    p_cal: float               # Model's calibrated probability
    ev_cents: float            # Expected value at time of trade
    # Settlement (filled post-hoc)
    settled: bool = False
    settlement_value: int = 0  # 0 or 100
    pnl_cents: float = 0.0    # Per-contract P&L


# =====================================================================
# ▸ TradingStrategy — abstract base (swap XGBoost for any model)
# =====================================================================

class TradingStrategy(ABC):
    """Abstract trading strategy for the backtester.

    Subclass this and implement ``generate_signals()`` to plug in any
    prediction model.  The backtest harness handles:
      - Data loading and feature engineering
      - Market snapshot lookup for execution prices
      - Settlement against CLI ground-truth
      - P&L computation
    """

    @abstractmethod
    def fit(
        self,
        X_train: pd.DataFrame,
        y_train: pd.Series,
    ) -> None:
        """Train the model on training data."""

    @abstractmethod
    def generate_signals(
        self,
        feature_row: pd.Series,
        custom_intraday_max_f: float,
        strikes: list[float],
        market_snapshot: pd.DataFrame,
        event_ticker: str,
        climate_date: str,
    ) -> list[TradeSignal]:
        """Generate trade signals for one observation.

        Parameters
        ----------
        feature_row :          One row of features (from FeatureEngine).
        custom_intraday_max_f: Running max temperature in °F.
        strikes :              Available Kalshi strike temperatures.
        market_snapshot :      Market data (yes_bid, yes_ask, etc.) at this time.
        event_ticker :         The Kalshi event ticker for this day.
        climate_date :         LST climate date string.

        Returns
        -------
        List of TradeSignal objects.  Empty list = no trades.
        """


# =====================================================================
# ▸ XGBoostEVStrategy — the default Weather Brain strategy
# =====================================================================

class XGBoostEVStrategy(TradingStrategy):
    """Trade based on XGBoost quantile regression expected value.

    This is the Weather Brain strategy from MODELING_IDEA.MD §8–§10:
    1. Predict the remaining heating delta distribution (7 quantiles)
    2. Map onto Kalshi strikes → P(High ≥ S)
    3. Compare calibrated P to market price → EV
    4. Trade when EV > threshold
    """

    def __init__(
        self,
        alphas: tuple[float, ...] = QUANTILE_ALPHAS,
        xgb_params: Optional[dict] = None,
        ev_threshold_cents: float = 3,
        max_contracts: int = 10,
    ):
        self.alphas = alphas
        self.xgb_params = xgb_params
        self.ev_threshold = ev_threshold_cents
        self.max_contracts = max_contracts
        self.suite: Optional[QuantileSuite] = None
        self.calibrator: Optional[IsotonicCalibrator] = None
        self._mapper = MonotonicMapper(alphas)
        self._pricer = StrikePricer(alphas)

    def fit(self, X_train: pd.DataFrame, y_train: pd.Series) -> None:
        """Train the quantile suite + calibrator on training data."""
        # Split: 80% for XGBoost, 20% for early stopping validation
        n = len(X_train)
        es_split = max(1, int(n * 0.8))
        X_tr, y_tr = X_train.iloc[:es_split], y_train.iloc[:es_split]
        X_v,  y_v  = X_train.iloc[es_split:], y_train.iloc[es_split:]

        self.suite = QuantileSuite(alphas=self.alphas, xgb_params=self.xgb_params)
        self.suite.fit(X_tr, y_tr, X_v, y_v)

        # Calibrator: use validation set predictions as calibration data
        if len(X_v) > 5:
            raw_preds_cal = self.suite.predict(X_v)
            mono_preds_cal = self._mapper.transform_batch(raw_preds_cal)
            p_raw_cal = mono_preds_cal[0.50].astype(np.float64)
            y_binary_cal = (y_v.values > 0).astype(np.float64)

            self.calibrator = IsotonicCalibrator()
            try:
                self.calibrator.fit(
                    p_raw_cal=p_raw_cal,
                    y_outcome_cal=y_binary_cal,
                    train_index=X_tr.index,
                    cal_index=X_v.index,
                )
                logger.info("Calibrator fitted on %d validation rows.", len(X_v))
            except Exception as e:
                logger.warning("Calibrator fitting failed: %s — using raw probs.", e)
                self.calibrator = None
        else:
            logger.info("Not enough validation data for calibrator — using raw probs.")
            self.calibrator = None

    def generate_signals(
        self,
        feature_row: pd.Series,
        custom_intraday_max_f: float,
        strikes: list[float],
        market_snapshot: pd.DataFrame,
        event_ticker: str,
        climate_date: str,
    ) -> list[TradeSignal]:
        if self.suite is None:
            raise RuntimeError("Must call fit() before generate_signals().")

        # Stage 2: Quantile predictions
        raw_q = self.suite.predict_row(feature_row)

        # Stage 3: Monotonic mapping
        mono_q = self._mapper.transform(raw_q)

        # Stage 4: Strike pricing
        p_raw = self._pricer.price_all_strikes(strikes, custom_intraday_max_f, mono_q)

        # Stage 5: Calibration
        p_cal: dict[float, float] = {}
        if self.calibrator is not None and self.calibrator.is_fitted:
            for s, p in p_raw.items():
                p_cal[s] = float(self.calibrator.calibrate(p))
        else:
            p_cal = dict(p_raw)

        # Stage 6: Generate signals based on EV vs market prices
        signals: list[TradeSignal] = []
        obs_time = feature_row.get("observation_time_utc")

        for strike, p in p_cal.items():
            # Find matching market ticker in snapshot
            matching = market_snapshot[
                market_snapshot["subtitle"].apply(
                    lambda x: _subtitle_matches_strike(x, strike)
                )
            ]
            if matching.empty:
                continue

            row = matching.iloc[-1]  # Most recent snapshot
            yes_ask = float(row.get("yes_ask", 100))
            no_ask = float(row.get("no_ask", 100))
            market_ticker = str(row.get("market_ticker", ""))

            # EV calculations (in cents) — subtract Kalshi taker fee (formula-based)
            fee_yes = _kalshi_taker_fee_cents(1, yes_ask)
            fee_no = _kalshi_taker_fee_cents(1, no_ask)
            ev_yes = (p * 100) - yes_ask - fee_yes
            ev_no = ((1 - p) * 100) - no_ask - fee_no

            # Pick the better side if EV > threshold
            if ev_yes >= self.ev_threshold and ev_yes >= ev_no:
                signals.append(TradeSignal(
                    climate_date=climate_date,
                    observation_time_utc=obs_time,
                    event_ticker=event_ticker,
                    market_ticker=market_ticker,
                    strike_f=strike,
                    side="yes",
                    p_cal=p,
                    ev_cents=ev_yes,
                    max_contracts=self.max_contracts,
                ))
            elif ev_no >= self.ev_threshold:
                signals.append(TradeSignal(
                    climate_date=climate_date,
                    observation_time_utc=obs_time,
                    event_ticker=event_ticker,
                    market_ticker=market_ticker,
                    strike_f=strike,
                    side="no",
                    p_cal=p,
                    ev_cents=ev_no,
                    max_contracts=self.max_contracts,
                ))

        return signals


